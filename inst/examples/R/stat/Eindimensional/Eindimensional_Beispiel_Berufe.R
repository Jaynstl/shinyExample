beruf <- c(16808, 14568, 2511, 3037, 522)
names (beruf) <- c("Angestellte", "Arbeiter",  "Beamte", "Selbstst\u00E4ndige", "Mith. Fam.ang.")
perc <- sapply(X=beruf,FUN=function(x) x/sum(beruf)*100)
rp <- round (perc)
names (beruf) <- paste (names(beruf), "\n", rp,"%")
par(mfrow=c(1,2),mar=c(5.1, 1.1, 4.1, 1.1))
par(mfg=c(1,1),mar=c(5.1, 1.1, 4.1, 3.6))
pie(beruf, col=c("dodgerblue", "tomato2", "yellow1", "springgreen3", "lightpink"), cex=1, main="Kreisdiagramm")
par(mfg=c(1,2),mar=c(5.1, 8.1, 4.1, 1.1))
barplot(axes=F, beruf, col=c("dodgerblue", "tomato2", "yellow1", "springgreen3", "lightpink"), main="S\u00E4ulendiagramm", ylim=c(0, 18000),
        xlab="Stellung im Beruf", ylab="Erwerbst\u00E4tige in Tsd.", cex.names=0.7, las=1, cex.axis=0.7)
axis(side=1, tck=F, labels=F)
axis(side=2, at=c(0, 2000, 4000, 6000, 8000, 10000, 12000, 14000, 16000, 18000), las=1, cex.axis=0.7)